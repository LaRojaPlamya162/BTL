Map::Map()
{
    dirt = TextureManager::LoadTexture("dirt.png");
    grass = TextureManager::LoadTexture("grass.png");
    water = TextureManager::LoadTexture("water.png");
    LoadMap(lvl1);
    MapSrcRect.x = 0;
    MapSrcRect.y = 0;
    MapSrcRect.w = 40;
    MapDestRect.w = 40;
    MapSrcRect.h = 40;
    MapDestRect.h = 40;
    MapDestRect.x = 0;
    MapDestRect.y = 0;
}
void Map::LoadMap(int arr[20][25])
{
    for (int row = 0; row <20;row++)
    {
        for (int column = 0; column <25;column++)
        {
            map[row][column] = arr[row][column];
        }
    }
}
void Map::DrawMap()
{
    int type = 0;
    for (int row = 0; row <25;row++)
    {
        for (int column = 0; column <25;column++)
        {
            type = map[row][column];
            MapDestRect.x = column * 40;
            MapDestRect.y = row * 40;
            switch(type)
            {
            case 0:
                TextureManager::Draw(dirt,MapSrcRect,MapDestRect);
                break;
            case 1:
                TextureManager::Draw(grass,MapSrcRect,MapDestRect);
                break;
            case 2:
                TextureManager::Draw(water,MapSrcRect,MapDestRect);
                break;
            default :
                break;
            }

        }
    }
}

